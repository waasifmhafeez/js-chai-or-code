const month = 3
switch (month) {
    case 1:
        console.log("Month is january.");
        break;
    case 2:
        console.log("Month is febuary.");
        break;
    case 3:
        console.log("Month is march.");
        break;

    default:
        console.log("no case matched");
        break;
}